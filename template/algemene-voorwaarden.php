					begrepen) of diensten, te allen tijde opzeggen met inachtneming van daartoe overeengekomen opzeggingsregels en een opzegtermijn van ten hoogste één maand.
				</p>
				<p>
					2. De consument kan een overeenkomst die voor bepaalde tijd is aangegaan en die strekt tot het geregeld afleveren van producten (elektriciteit daaronder
					begrepen) of diensten, te allen tijde tegen het einde van de bepaalde duur opzeggen met inachtneming van daartoe overeengekomen opzeggingsregels en een
					opzegtermijn van ten hoogste één maand.
				</p>
				<p>
					3. De consument kan de in de vorige leden genoemde overeenkomsten:
				</p>
				<p>
					- te allen tijde opzeggen en niet beperkt worden tot opzegging op een bepaald tijdstip of in een bepaalde periode;
				</p>
				<p>
					- tenminste opzeggen op dezelfde wijze als zij door hem zijn aangegaan;
				</p>
				<p>
					- altijd opzeggen met dezelfde opzegtermijn als de ondernemer voor zichzelf heeft bedongen.
				</p>
				<p>
					<em>Verlenging:</em>
				</p>
				<p>
					4. Een overeenkomst die voor bepaalde tijd is aangegaan en die strekt tot het geregeld afleveren van producten (elektriciteit daaronder begrepen) of
					diensten, mag niet stilzwijgend worden verlengd of vernieuwd voor een bepaalde duur.
				</p>
				<p>
					5. In afwijking van het vorige lid mag een overeenkomst die voor bepaalde tijd is aangegaan en die strekt tot het geregeld afleveren van dag- nieuws- en
					weekbladen en tijdschriften stilzwijgend worden verlengd voor een bepaalde duur van maximaal drie maanden, als de consument deze verlengde overeenkomst
					tegen het einde van de verlenging kan opzeggen met een opzegtermijn van ten hoogste één maand.
				</p>
				<p>
					6. Een overeenkomst die voor bepaalde tijd is aangegaan en die strekt tot het geregeld afleveren van producten of diensten, mag alleen stilzwijgend voor
					onbepaalde duur worden verlengd als de consument te allen tijde mag opzeggen met een opzegtermijn van ten hoogste één maand. De opzegtermijn is ten hoogste
					drie maanden in geval de overeenkomst strekt tot het geregeld, maar minder dan eenmaal per maand, afleveren van dag-, nieuws- en weekbladen en
					tijdschriften.
				</p>
				<p>
					7. Een overeenkomst met beperkte duur tot het geregeld ter kennismaking afleveren van dag-, nieuws- en weekbladen en tijdschriften (proef- of
					kennismakingsabonnement) wordt niet stilzwijgend voortgezet en eindigt automatisch na afloop van de proef- of kennismakingsperiode.
				</p>
				<p>
					<em>Duur:</em>
				</p>
				<p>
					8. Als een overeenkomst een duur van meer dan een jaar heeft, mag de consument na een jaar de overeenkomst te allen tijde met een opzegtermijn van ten
					hoogste één maand opzeggen, tenzij de redelijkheid en billijkheid zich tegen opzegging vóór het einde van de overeengekomen duur verzetten.
				</p>
