				</p>
				<p>
					4. Pakketreizen als bedoeld in artikel 7:500 BW en overeenkomsten van personenvervoer;
				</p>
				<p>
					5. Dienstenovereenkomsten voor terbeschikkingstelling van accommodatie, als in de overeenkomst een bepaalde datum of periode van uitvoering is voorzien en
					anders dan voor woondoeleinden, goederenvervoer, autoverhuurdiensten en catering;
				</p>
				<p>
					6. Overeenkomsten met betrekking tot vrijetijdsbesteding, als in de overeenkomst een bepaalde datum of periode van uitvoering daarvan is voorzien;
				</p>
				<p>
					7. Volgens specificaties van de consument vervaardigde producten, die niet geprefabriceerd zijn en die worden vervaardigd op basis van een individuele
					keuze of beslissing van de consument, of die duidelijk voor een specifieke persoon bestemd zijn;
				</p>
				<p>
					8. Producten die snel bederven of een beperkte houdbaarheid hebben;
				</p>
				<p>
					9. Verzegelde producten die om redenen van gezondheidsbescherming of hygiëne niet geschikt zijn om te worden teruggezonden en waarvan de verzegeling na
					levering is verbroken;
				</p>
				<p>
					10. Producten die na levering door hun aard onherroepelijk vermengd zijn met andere producten;
				</p>
				<p>
					11. Alcoholische dranken waarvan de prijs is overeengekomen bij het sluiten van de overeenkomst, maar waarvan de levering slechts kan plaatsvinden na 30
					dagen, en waarvan de werkelijke waarde afhankelijk is van schommelingen van de markt waarop de ondernemer geen invloed heeft;
				</p>
				<p>
					12. Verzegelde audio-, video-opnamen en computerprogrammatuur, waarvan de verzegeling na levering is verbroken;
				</p>
				<p>
					13. Kranten, tijdschriften of magazines, met uitzondering van abonnementen hierop;
				</p>
				<p>
					14. De levering van digitale inhoud anders dan op een materiële drager, maar alleen als:
				</p>
				<p>
					a. de uitvoering is begonnen met uitdrukkelijke voorafgaande instemming van de consument; en
				</p>
				<p>
					b. de consument heeft verklaard dat hij hiermee zijn herroepingsrecht verliest.
				</p>
				<p>
					<strong>Artikel 11 </strong>
					<strong>-</strong>
					<strong> De prijs</strong>
				</p>
				<p>
					1. Gedurende de in het aanbod vermelde geldigheidsduur worden de prijzen van de aangeboden producten en/of diensten niet verhoogd, behoudens
					prijswijzigingen als gevolg van veranderingen in btw-tarieven.
				</p>
				<p>
					2. In afwijking van het vorige lid kan de ondernemer producten of diensten waarvan de prijzen gebonden zijn aan schommelingen op de financiële markt en
					waar de ondernemer geen invloed op heeft, met variabele prijzen aanbieden. Deze gebondenheid aan schommelingen en het feit dat eventueel vermelde prijzen
					richtprijzen zijn, worden bij het aanbod vermeld.
				</p>
				<p>
					3. Prijsverhogingen binnen 3 maanden na de totstandkoming van de overeenkomst zijn alleen toegestaan indien zij het gevolg zijn van wettelijke regelingen
					of bepalingen.
				</p>
				<p>
					4. Prijsverhogingen vanaf 3 maanden na de totstandkoming van de overeenkomst zijn alleen toegestaan indien de ondernemer dit bedongen heeft en:
				</p>
				<p>
					a. deze het gevolg zijn van wettelijke regelingen of bepalingen; of
				</p>
				<p>
					b. de consument de bevoegdheid heeft de overeenkomst op te zeggen met ingang van de dag waarop de prijsverhoging ingaat.
				</p>
				<p>
					5. De in het aanbod van producten of diensten genoemde prijzen zijn inclusief btw.
				</p>
				<p>
					<strong>Artikel 12 </strong>
					<strong>-</strong>
					<strong> Nakoming overeenkomst en extra garantie </strong>
				</p>
				<p>
					1. De ondernemer staat er voor in dat de producten en/of diensten voldoen aan de overeenkomst, de in het aanbod vermelde specificaties, aan de redelijke
					eisen van deugdelijkheid en/of bruikbaarheid en de op de datum van de totstandkoming van de overeenkomst bestaande wettelijke bepalingen en/of
					overheidsvoorschriften. Indien overeengekomen staat de ondernemer er tevens voor in dat het product geschikt is voor ander dan normaal gebruik.
				</p>
				<p>
					2. Een door de ondernemer, diens toeleverancier, fabrikant of importeur verstrekte extra garantie beperkt nimmer de wettelijke rechten en vorderingen die
					de consument op grond van de overeenkomst tegenover de ondernemer kan doen gelden indien de ondernemer is tekortgeschoten in de nakoming van zijn deel van
					de overeenkomst.
				</p>
				<p>
					3. Onder extra garantie wordt verstaan iedere verbintenis van de ondernemer, diens toeleverancier, importeur of producent waarin deze aan de consument
					bepaalde rechten of vorderingen toekent die verder gaan dan waartoe deze wettelijk verplicht is in geval hij is tekortgeschoten in de nakoming van zijn
					deel van de overeenkomst.
				</p>
				<p>
					<strong>Artikel 13 </strong>
					<strong>-</strong>
					<strong> Levering en uitvoering</strong>
				</p>
				<p>
					1. De ondernemer zal de grootst mogelijke zorgvuldigheid in acht nemen bij het in ontvangst nemen en bij de uitvoering van bestellingen van producten en
					bij de beoordeling van aanvragen tot verlening van diensten.
				</p>
				<p>
					2. Als plaats van levering geldt het adres dat de consument aan de ondernemer kenbaar heeft gemaakt.
				</p>
				<p>
