					herroeping niet heeft verstrekt, of;
				</p>
				<p>
					b. de consument niet uitdrukkelijk om de aanvang van de uitvoering van de dienst of levering van gas, water, elektriciteit of stadsverwarming tijdens de
					bedenktijd heeft verzocht.
				</p>
