					<strong>Artikel 9 - Verplichtingen van de ondernemer bij herroeping</strong>
				</p>
				<p>
					1. Als de ondernemer de melding van herroeping door de consument op elektronische wijze mogelijk maakt, stuurt hij na ontvangst van deze melding onverwijld
					een ontvangstbevestiging.
				</p>
				<p>
					2. De ondernemer vergoedt alle betalingen van de consument, inclusief eventuele leveringskosten door de ondernemer in rekening gebracht voor het
					geretourneerde product, onverwijld doch binnen 14 dagen volgend op de dag waarop de consument hem de herroeping meldt. Tenzij de ondernemer aanbiedt het
					product zelf af te halen, mag hij wachten met terugbetalen tot hij het product heeft ontvangen of tot de consument aantoont dat hij het product heeft
					teruggezonden, naar gelang welk tijdstip eerder valt.
				</p>
				<p>
					3. De ondernemer gebruikt voor terugbetaling hetzelfde betaalmiddel dat de consument heeft gebruikt, tenzij de consument instemt met een andere methode. De
					terugbetaling is kosteloos voor de consument.
				</p>
				<p>
					4. Als de consument heeft gekozen voor een duurdere methode van levering dan de goedkoopste standaardlevering, hoeft de ondernemer de bijkomende kosten
					voor de duurdere methode niet terug te betalen.
				</p>
				<p>
					<strong>Artikel 10 </strong>
					<strong>-</strong>
					<strong> Uitsluiting herroepingsrecht</strong>
				</p>
				<p>
					De ondernemer kan de navolgende producten en diensten uitsluiten van het herroepingsrecht, maar alleen als de ondernemer dit duidelijk bij het aanbod,
					althans tijdig voor het sluiten van de overeenkomst, heeft vermeld:
				</p>
				<p>
					1. Producten of diensten waarvan de prijs gebonden is aan schommelingen op de financiële markt waarop de ondernemer geen invloed heeft en die zich binnen
					de herroepingstermijn kunnen voordoen;
				</p>
				<p>
					2. Overeenkomsten die gesloten zijn tijdens een openbare veiling. Onder een openbare veiling wordt verstaan een verkoopmethode waarbij producten, digitale
					inhoud en/of diensten door de ondernemer worden aangeboden aan de consument die persoonlijk aanwezig is of de mogelijkheid krijgt persoonlijk aanwezig te
					zijn op de veiling, onder leiding van een veilingmeester, en waarbij de succesvolle bieder verplicht is de producten, digitale inhoud en/of diensten af te
					nemen;
				</p>
				<p>
					3. Dienstenovereenkomsten, na volledige uitvoering van de dienst, maar alleen als:
				</p>
				<p>
					a. de uitvoering is begonnen met uitdrukkelijke voorafgaande instemming van de consument; en
				</p>
